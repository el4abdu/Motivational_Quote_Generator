// Function to translate categories based on the selected language
function translateCategory(category, language) {
  // This is a simplified mapping for demonstration
  // In a real app, you would want a more comprehensive mapping
  const categoryTranslations = {
    'success': {
      'fr': 'succès',
      'ar': 'نجاح'
    },
    'happiness': {
      'fr': 'bonheur',
      'ar': 'سعادة'
    },
    'work': {
      'fr': 'travail',
      'ar': 'عمل'
    },
    'love': {
      'fr': 'amour',
      'ar': 'حب'
    },
    'life': {
      'fr': 'vie',
      'ar': 'حياة'
    },
    'motivation': {
      'fr': 'motivation',
      'ar': 'تحفيز'
    },
    'courage': {
      'fr': 'courage',
      'ar': 'شجاعة'
    },
    'dreams': {
      'fr': 'rêves',
      'ar': 'أحلام'
    },
    'time': {
      'fr': 'temps',
      'ar': 'وقت'
    },
    'mind': {
      'fr': 'esprit',
      'ar': 'عقل'
    }
    // Add more translations as needed
  };

  if (language === 'en' || !categoryTranslations[category] || !categoryTranslations[category][language]) {
    return category.charAt(0).toUpperCase() + category.slice(1);
  }

  const translated = categoryTranslations[category][language];
  return language === 'ar' ? translated : translated.charAt(0).toUpperCase() + translated.slice(1);
} 